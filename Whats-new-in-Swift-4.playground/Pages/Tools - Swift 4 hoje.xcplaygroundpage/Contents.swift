 # Como usar Swift 4 hoje
 
 - Xcode 9 tem Swift 4 como default
 - Xcode 8.3?
 

1. Faça download [do último snapshot do Swift 4.0 em swift.org][Snapshot downloads].
1. Execute/Rode o instalador
1. No Xcode, vá em _Xcode > Toolchains > Manage Toolchains…_ e escolha o snapshot do Swift 4

 
 ![easy][xcode-8-3-toolchain-dialog]


 ## Detalhes, detalhes...
 
 
 - Reinicie o Xcode caso tenha alguns problemas com syntax highlighting e code completion
 - Se quiser trocar entre múltiplas versões do Swift na linha de comando (como para executar Swift 4 no REPL ou gerar pacotes com Swift 4), pode ser usado o [swiftenv][swiftenv] do Kyle Fuller.

